#!/usr/bin/env python3

"""
This module provides a function to convert a float to a string.

Functions:
- to_str(n: float) -> str: Converts a floating-point number to its
  string representation.

Example:
>>> to_str(3.14)
'3.14'
"""


def to_str(n: float) -> str:
    """
    Convert a float to a string.

    Parameters:
    n (float): The floating-point number to convert.

    Returns:
    str: The string representation of the float.

    Example:
    >>> to_str(3.14)
    '3.14'
    """
    return str(n)
